sub get_xy {
        my ($tmp);
        $tmp = (1 / 2 ** (4 - $_[1] ));
        $wh = $yw * $tmp;
	$ww = $xw * $tmp;
        $y1 = $my - $wh / 2;
        $y2 = $y1 + $wh;
	$xl = $mx - (($ww * 5) /2);
	$x1 = $xl + $_[0] * $ww;
	$x2 = $x1 + $ww;
}


